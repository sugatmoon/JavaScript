'use strict'; // it is use when we want to declare any variable use keyword 'var'

var maha = 'Maharashtra';
switch ( maha ) {
    case 'Gujarat':   
        console.log( 'Gujarat');
    break;
    case 'MP' :
        console.log( 'Madhya Pradash' );
        break;
    case 'UP' :
        console.log( 'Utter Pradash' );
        break;
    case 'Maharashtra':
        console.log( 'Maharashtra' );
        break;
    default :
        console.log( 'NA' );
}